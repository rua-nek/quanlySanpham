    private void tbThongtinMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbThongtinMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbThongtin.getSelectedRow();
        if (selectedRow != -1) {
            // Lấy dữ liệu từ hàng đã chọn
            String masp = tbThongtin.getValueAt(selectedRow, 0).toString();
            String mancc = tbThongtin.getValueAt(selectedRow, 1).toString();
            String tensp = tbThongtin.getValueAt(selectedRow, 2).toString();
            String gia = tbThongtin.getValueAt(selectedRow, 3).toString();
            String soluong = tbThongtin.getValueAt(selectedRow, 4).toString();
            // Hành động cơ bản
            edtMaSP.setText(masp);
            jComboBox1.setSelectedItem(mancc);
            edtTenSP.setText(tensp);
            edtGia.setText(gia);
            edtSoLuong.setText(soluong);
        }
        
    }//GEN-LAST:event_tbThongtinMouseClicked

