    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String masp = edtMaSP.getText();
            String mancc = jComboBox1.getSelectedItem().toString();
            String tensp = edtTenSP.getText();
            String gia = edtGia.getText();
            String soluong = edtSoLuong.getText();
            Connection conn = dbconnect.getConnection();
            String sql = "INSERT INTO SanPham (MaSP, MaNCC, TenSP, Gia, SoLuong) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, masp);
            pst.setString(2, mancc);
            pst.setString(3, tensp);
            pst.setString(4, gia);
            pst.setString(5, soluong);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadData();
            clearFields();
        } catch (Exception e) {
            e.printStackTrace();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed
    private boolean isEditing = false; // Biến để theo dõi trạng thái chỉnh sửa
    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(!isEditing){
            enableinputFields(true);
            isEditing = true;
        }
        else{
            try {
                String masp = edtMaSP.getText();
                String mancc = jComboBox1.getSelectedItem().toString();
                String tensp = edtTenSP.getText();
                String gia = edtGia.getText();
                String soluong = edtSoLuong.getText();
                
                Connection conn = dbconnect.getConnection();
                String sql = "UPDATE SanPham SET MaNCC=?, TenSP=?, Gia=?, SoLuong=? WHERE MaSP=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, mancc);
                pst.setString(2, tensp);
                pst.setString(3, gia);
                pst.setString(4, soluong);
                pst.setString(5, masp);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Chinh sua thanh cong");
                loadData();
                clearFields();
                enableinputFields(false);
                isEditing = false;
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

