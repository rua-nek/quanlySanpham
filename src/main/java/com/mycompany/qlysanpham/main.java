    private void loadData() {
    // Clear existing data in the table
    DefaultTableModel model = (DefaultTableModel) tbThongtin.getModel();
    model.setRowCount(0);
    
    // Using try-with-resources for proper resource management
    try (Connection conn = dbconnect.getConnection();
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery("SELECT * FROM sanpham")) {
        // Iterate through the ResultSet and add each row to the table model
        while (rs.next()) {
            Object[] row = {
                rs.getString("masp"),
                rs.getString("mancc"),
                rs.getString("tensp"),
                rs.getString("gia"),
                rs.getString("soluong")
            };
            model.addRow(row);
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    private void loadComboBox(JComboBox<String> comboBox) {
    // Xóa các item hiện tại trong combo box
    comboBox.removeAllItems();

    // Kết nối và truy vấn cơ sở dữ liệu
    try (Connection conn = dbconnect.getConnection();
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery("SELECT * FROM Nhacungcap")) {

        // Duyệt và thêm các mã nhà cung cấp vào combo box
        while (rs.next()) {
            comboBox.addItem(rs.getString("mancc"));
        }

        // Không chọn gì sau khi load xong
        comboBox.setSelectedIndex(-1);

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Lỗi tải dữ liệu: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
    }
}
